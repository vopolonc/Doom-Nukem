#include "libft.h"

int	ft_sqrt(int nb)
{
	int del;

	del = 1;
	if (nb < 0)
		return (0);
	while (del * del <= nb)
	{
		if (nb == del * del)
			return (del);
		del++;
	}
	return (0);
}
