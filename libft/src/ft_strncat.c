#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t ln)
{
	char	*rs;
	size_t	i;

	rs = s1;
	i = 1;
	while (i)
	{
		i = 0;
		if (*s1 == '\0')
		{
			while (*s2 && ln-- > 0)
				*s1++ = *s2++;
			*s1 = '\0';
		}
		else
		{
			s1++;
			i = 1;
		}
	}
	return (rs);
}
