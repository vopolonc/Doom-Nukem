#include "libft.h"

char	*ft_strnset(char *s, char c, size_t n)
{
	char *fnl;

	if (s)
	{
		fnl = s;
		while (*s && n-- > 0)
		{
			*s = c;
			s++;
		}
		return (fnl);
	}
	return (NULL);
}
